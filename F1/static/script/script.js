// Função para obter nome da bandeira
function obterNomeBandeira(pais) {
    return mapeamentoBandeiras[pais] || pais.toLowerCase().replace(/\s+/g, '_');
}

// Função para obter o nome do arquivo da pista
function obterNomePista(circuitId) {
    // Se existe um mapeamento específico, usa ele
    if (mapeamentoPistas[circuitId]) {
        return mapeamentoPistas[circuitId];
    }
    
    // Caso contrário, tenta usar o circuitId diretamente
    return `${circuitId}.png`;
}

// Função para carregar as corridas
function carregarCorridas() {
    const container = document.getElementById('corridas-container');
    
    // Limpa o container
    container.innerHTML = '';
    
    // Para cada corrida no array, cria um elemento
    corridasData.race.forEach(corrida => {
        const corridaElement = criarElementoCorrida(corrida);
        container.appendChild(corridaElement);
    });
}

// Função para criar o elemento HTML de uma corrida
function criarElementoCorrida(corrida) {
    const div = document.createElement('div');
    div.className = 'corrida';
    
    // Formata a data
    const dataCorrida = new Date(corrida.schedule.race.date);
    const dataFormatada = dataCorrida.toLocaleDateString('pt-BR');
    
    div.innerHTML = `
        <p class="round">Round ${corrida.round}</p>
        <div class="bandeira-container">
            <img src="./assets/Bandeiras/${corrida.circuit.country.toLowerCase().replace(/\s+/g, '_')}.png" 
                alt="Bandeira ${corrida.circuit.country}" 
                class="bandeira"
                onerror="this.style.display='none'">
        </div>

        <h2 class="pais">${corrida.circuit.country}</h2>
        
        <p class="circuit-name">${corrida.circuit.circuitName}</p>
        
        <p class="data">${dataFormatada}</p>

        <div class="pista-container">
            <img src="./assets/Pistas/${obterNomePista(corrida.circuit.circuitId)}" 
                alt="Pista ${corrida.circuit.circuitName}" 
                class="pista"
                onerror="this.style.display='none'">
        </div>
        
        <div class="winner">
            <strong>Vencedor:</strong><br>
            ${corrida.winner.name} ${corrida.winner.surname}<br>
            <small>${corrida.teamWinner.teamName}</small>
        </div>
    `;
    
    return div;
}


// Função para tratar erro de imagem
